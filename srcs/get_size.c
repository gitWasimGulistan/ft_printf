#include "../includes/ft_printf.h"

void		get_size(t_env *e)
{
	while (*(e->s) == 'h' && (*(e->s + 1) == 'h'))
	{
		SIZE = 1;
		HH = (*(e->s) == 'h') ? 1 : HH;
		e->s++;
	}
	while (*(e->s) == 'l' && (*(e->s + 1) == 'l'))
	{
		SIZE = 1;
		LL = (*(e->s) == 'l') ? 1 : LL;
		e->s++;
	}
	while (check_char(*(e->s), "hljz"))
	{
		SIZE = 1;
		H = (*(e->s) == 'h') ? 1 : H;
		L = (*(e->s) == 'l') ? 1 : L;
		J = (*(e->s) == 'j') ? 1 : J;
		Z = (*(e->s) == 'z') ? 1 : Z;
		e->s++;
	}
}
